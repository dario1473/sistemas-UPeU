package Examen;

public class Tipodeexamen {
	
	private String ingenierias;
	private String sociales;
	private String biomedicas;
	
	
public String getIngenierias() {
		return ingenierias;
	}
	public void setIngenierias(String ingenierias) {
		this.ingenierias = ingenierias;
	}
	public String getSociales() {
		return sociales;
	}
	public void setSociales(String sociales) {
		this.sociales = sociales;
	}
	public String getBiomedicas() {
		return biomedicas;
	}
	public void setBiomedicas(String biomedicas) {
		this.biomedicas = biomedicas;
	}

}
