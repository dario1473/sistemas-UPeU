package Examen2;

public class Cargo {
  
	private String secretaria;
	private String limpeza;
	private String docente;
	private String jardineria;
	private String cafetin ;
	
	public String getSecretaria() {
		return secretaria;
	}
	public void setSecretaria(String secretaria) {
		this.secretaria = secretaria;
	}
	public String getLimpeza() {
		return limpeza;
	}
	public void setLimpeza(String limpeza) {
		this.limpeza = limpeza;
	}
	public String getDocente() {
		return docente;
	}
	public void setDocente(String docente) {
		this.docente = docente;
	}
	public String getJardineria() {
		return jardineria;
	}
	public void setJardineria(String jardineria) {
		this.jardineria = jardineria;
	}
	public String getCafetin() {
		return cafetin;
	}
	public void setCafetin(String cafetin) {
		this.cafetin = cafetin;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
